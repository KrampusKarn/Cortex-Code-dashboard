-- 03_silver.sql  --  SILVER layer: typed tables + Bronze->Silver flatten
--
-- The "Transform" of the medallion. Defines SILVER.* (typed/cleaned tables, DDL
-- mirrors the captured 00_setup.sql data tables) and flattens each BRONZE.* VARIANT
-- table into it. Driven by BRONZE.BRONZE_ENDPOINTS + SILVER.SILVER_FIELD_MAP (the
-- field-path map for the two NESTED endpoints; flat endpoints map automatically).
--
-- SAFE: SP_FLATTEN skips a table whose Bronze is empty, so a partial ingest never
-- wipes a Silver table. INSERT OVERWRITE makes Silver exactly reflect Bronze.
--
-- NOTE: SILVER.* mirrors the data-table DDL in 00_setup.sql — keep the two in sync.
-- Run as ACCOUNTADMIN.
-- =====================================================================
