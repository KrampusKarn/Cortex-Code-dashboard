-- 3) Flatten ONE Bronze table into its Silver table --------------------
CREATE OR REPLACE PROCEDURE SILVER.SP_FLATTEN(TABLE_NAME STRING)
  RETURNS STRING LANGUAGE SQL
AS
$$
DECLARE
  cnt INT DEFAULT 0;
  collist STRING;
  sel STRING;
  bronze_fqn STRING;
BEGIN
  bronze_fqn := 'DEMO_EMPLOYEE_APP.BRONZE.' || :TABLE_NAME;
  SELECT COUNT(*) INTO :cnt FROM IDENTIFIER(:bronze_fqn);
  IF (cnt = 0) THEN
    RETURN :TABLE_NAME || ': skipped (bronze empty)';
  END IF;
  SELECT
      LISTAGG(c.COLUMN_NAME, ', ') WITHIN GROUP (ORDER BY c.ORDINAL_POSITION),
      LISTAGG('PAYLOAD:' || COALESCE(m.JSON_PATH, LOWER(c.COLUMN_NAME))
              || '::string AS ' || c.COLUMN_NAME, ', ')
        WITHIN GROUP (ORDER BY c.ORDINAL_POSITION)
    INTO :collist, :sel
  FROM DEMO_EMPLOYEE_APP.INFORMATION_SCHEMA.COLUMNS c
  LEFT JOIN DEMO_EMPLOYEE_APP.SILVER.SILVER_FIELD_MAP m
    ON m.SILVER_TABLE = c.TABLE_NAME AND m.COLUMN_NAME = c.COLUMN_NAME
  WHERE c.TABLE_SCHEMA = 'SILVER' AND c.TABLE_NAME = :TABLE_NAME;

  EXECUTE IMMEDIATE
    'INSERT OVERWRITE INTO DEMO_EMPLOYEE_APP.SILVER.' || :TABLE_NAME || ' (' || :collist || ') ' ||
    'SELECT ' || :sel || ' FROM DEMO_EMPLOYEE_APP.BRONZE.' || :TABLE_NAME;
  RETURN :TABLE_NAME || ': ' || cnt || ' rows';
END;
$$;

-- 4) Driver: flatten every endpoint that has Bronze data ---------------
CREATE OR REPLACE PROCEDURE SILVER.SP_BUILD_SILVER()
  RETURNS STRING LANGUAGE SQL
AS
$$
DECLARE
  n INT DEFAULT 0;
  c CURSOR FOR SELECT TABLE_NAME FROM DEMO_EMPLOYEE_APP.BRONZE.BRONZE_ENDPOINTS;
BEGIN
  FOR r IN c DO
    LET tn STRING := r.TABLE_NAME;
    CALL DEMO_EMPLOYEE_APP.SILVER.SP_FLATTEN(:tn);
    n := n + 1;
  END FOR;
  RETURN 'flattened ' || n || ' tables (empty bronze skipped)';
END;
$$;

